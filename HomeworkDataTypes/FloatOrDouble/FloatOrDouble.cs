using System;


namespace FloatOrDouble
{
    class FloatOrDouble
    {
        static void Main(string[] args)
        {
            float a = 12.345f;
            float b = 3456.091f;
            double c = 34.567839023;
            double d = 8923.1234857;

            Console.WriteLine(" The values {0} and {1} \n can be assigned to a variable of type float. \n The values {2} and {3} \n can be assigned to a variable of type double.", a, b, c, d);


        }
    }
}
